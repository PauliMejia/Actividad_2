let numero1 = 10;
let numero2 = 2;
let operador = "+";

if (operador === "+") {
    console.log(numero1 + numero2);
} else if (operador === "*") {
    console.log(numero1 * numero2);
} else if (operador === "/") {
    console.log(numero1 / numero2);
} else if (operador === "-") {
    console.log(numero1 - numero2);
}